def add(a: float, b: float) -> float:
    """
    Add two numbers.

    This function can add two numbers.

    Args:
        a (float): Give the first number.
        b (float): Give the second number.

    Returns:
        float: Gvie the sum.

    Example:
        >>> add(5, 8)
        13
    """
    return a + b
# End of add
